    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        //Kiểm tra lỗi
        //Kiểm tra số CMND
        Pattern pattern = Pattern.compile("^[0-9]*$");
        Matcher matcher = pattern.matcher(txtSoCMND.getText());
        if (!matcher.matches()) {
            JOptionPane.showMessageDialog(this, "CMND phải là số và không có khoảng trắng");
            txtSoCMND.setText("");
        }
        //Kiểm tra trùng số CMND
        trungCMND();
        //Kiểm tra tên
        Matcher math = pattern.matcher(txtTen.getText());
        if (math.matches()) {
            JOptionPane.showMessageDialog(this, "Họ tên bạn nhập không đúng");
            txtTen.setText("");
        }
        //Kiểm tra ô trống
        if (txtTen.getText().equals("") || cboNamSinh.getSelectedItem() == "----"
                || cboGioiTinh.getSelectedItem() == "---" || txtSoCMND.getText().equals("")
                || txtQueQuan.getText().equals("") || txtChoOHienNay.getText().equals("")
                || cboNghe.getSelectedItem() == "-------") {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ");

        }
        else {
            add();
            XFile.writeObject("d:/congdans.dat", list);
        }

    }//GEN-LAST:event_btnLuuActionPerformed
