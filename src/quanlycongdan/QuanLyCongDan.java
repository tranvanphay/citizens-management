    List<CongDan> list = new ArrayList<>();
    public void add() {
        CongDan cd = new CongDan();
        cd.CMND = txtSoCMND.getText();
        cd.ten = txtTen.getText();
        cd.gioiTinh = cboGioiTinh.getSelectedItem().toString();
        cd.namSinh = cboNamSinh.getSelectedItem().toString();
        cd.queQuan = txtQueQuan.getText();
        cd.ngheNghiep = cboNghe.getSelectedItem().toString();
        cd.choOHienNay = txtChoOHienNay.getText();
        cd.hinhAnh=(ImageIcon) lblAnh.getIcon();

        list.add(cd);
    }

    public void delete() {
        int index = tblBang.getSelectedRow();
        list.remove(index);
    }

    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblBang.getModel();
        model.setRowCount(0);
        for (CongDan cd : list) {
            Object[] row = new Object[]{
                cd.CMND,
                cd.ten,
                cd.gioiTinh,
                cd.namSinh,
                cd.queQuan,
                cd.ngheNghiep,
                cd.choOHienNay,
                cd.hinhAnh
            };
            model.addRow(row);
        }
    }

